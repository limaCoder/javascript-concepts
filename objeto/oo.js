// Principios importantes:
// 1. abstracao - É utilizada para a definição de entidades do mundo real. Sendo onde são criadas as classes. Essas 
//entidades são consideradas tudo que é real, tendo como consideração as suas características(atributos) 
// e ações (métodos). Ou seja, abstrações do mundo real

// 2. encapsulamento - Em um processo de encapsulamento os atributos das classes são do tipo private. Para acessar 
// esses tipos de modificadores, é necessário criar métodos setters e getters. Por entendimento os métodos setters 
// servem para alterar a informação de uma propriedade de um objeto. E os métodos getters para retornar o valor 
// dessa propriedade. É a técnica utilizada para esconder uma ideia, ou seja, não expôr detalhes internos para o 
// usuário, tornando partes do sistema mais independentes possível.

// 3. herança (prototype) - A herança ocorre quando uma classe herda atributos ou métodos de sua superclasse, ou seja, 
// o comportamento de sua superclasse/classe-mãe.

// 4. polimorfismo -  ocorre quando uma classe redefine o comportamento de sua superclasse, sobrescrevendo métodos 
// ou alterando o valor de atributos, por exemplo. Ou seja, o polimorfismo pode ocorrer à partir da herança.

